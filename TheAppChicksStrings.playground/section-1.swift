// / is a special character that indicates something special is coming next in a string
println("list: 1 - Apple 2 - Bananas 6 - Oranges") // not neatly organized list

// What if you wanted to print each item in this list on a new line... use /n
// click on View -> Assistant Editor to see Console Output
// use \t for a tab.. replace the dash
println("list:\n 1 \t Apple\n 2 \t Bananas\n 6 \t Oranges") //  neatly organized list

// remove all spaces
println("list:\n1\tApple\n2\tBananas\n6\tOranges") //  neatly organized list

